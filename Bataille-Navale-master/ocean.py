# def des differents tableaux (user, bot, tableau bot vu par l'utilisateur) ----

# user -------------------------------------------------------------------------
def oceanUser(l):
    for i in range(len(l)):
        print("\n", l[i], )
    return ""

# bot --------------------------------------------------------------------------
def oceanBot(m):
    for i in range(len(m)):
        print("\n", m[i], )
    return ""

# bot vu par user --------------------------------------------------------------
def oceanBotForUser(p):
    for i in range(len(p)):
        print("\n", p[i], )
    return ""
